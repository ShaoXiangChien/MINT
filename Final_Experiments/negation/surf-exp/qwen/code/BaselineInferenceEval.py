def load_vlm(model_path: str, device: str) -> ModelAndTokenizer:
	"""
	Load the Qwen2.5-VL model and processor.
	
	Args:
		model_path: HuggingFace model ID or local path to the model
		device: Device to load the model on (e.g., 'cuda:0' or 'cpu')
	
	Returns:
		ModelAndTokenizer object containing the model and processor
	"""
	# Load model with automatic dtype selection and device mapping
	model = Qwen2_5_VLForConditionalGeneration.from_pretrained(
		model_path, torch_dtype="auto", device_map="auto"
	)
	# Load the processor for handling text and image inputs
	processor = AutoProcessor.from_pretrained(model_path)

	# Wrap in ModelAndTokenizer helper class for consistency with existing code
	mt = ModelAndTokenizer(
		model_path,
		model=model,
		low_cpu_mem_usage=False,
		device=device,
	)
	mt.processor = processor
	# Set model to evaluation mode (disables dropout, etc.)
	mt.model.eval()
	return mt


def load_category_mapping(mapping_path: str) -> Dict[str, str]:
	"""
	Load the JSON file that maps category IDs to category names.
	
	Args:
		mapping_path: Path to the JSON file containing the mapping
	
	Returns:
		Dictionary mapping category IDs (as strings) to category names
	"""
	with open(mapping_path, "r") as f:
		category_mapping = json.load(f)
	return category_mapping


def get_category_from_filename(filename: str) -> str:
	"""
	Extract category ID from filename.
	
	Expected format: 'categoryID_imageID.jpg' (e.g., '1000_000000508602.jpg')
	
	Args:
		filename: Name of the image file
	
	Returns:
		Category ID as a string
	"""
	base = os.path.splitext(filename)[0]  # Remove file extension
	category_id = base.split("_")[0] if "_" in base else "0"  # Extract first part
	return category_id


def evaluate_response(response_text: str, category: str) -> int:
	"""
	Convert model's free-form text response into a binary prediction.
	
	This function implements simple heuristics to interpret the model's answer:
	- If response contains "yes" but not "no", predict 1 (present)
	- If response contains "no" but not "yes", predict 0 (absent)
	- Otherwise, check if the category name appears in the response
	
	Args:
		response_text: The text generated by the model
		category: The category name being queried
	
	Returns:
		Binary prediction: 1 if object is present, 0 if absent
	"""
	response_lower = response_text.lower().strip()
	
	# Check for explicit yes/no answers
	if "yes" in response_lower and "no" not in response_lower:
		return 1
	if "no" in response_lower and "yes" not in response_lower:
		return 0
	
	# Fall back to checking if category name is mentioned
	return 1 if category.lower() in response_lower else 0


def run_single_inference(
	mt: ModelAndTokenizer,
	image_path: str,
	prompt: str,
	max_new_tokens: int = 20
) -> str:
	"""
	Perform inference on a single image with the given prompt.
	
	Args:
		mt: ModelAndTokenizer object containing model and processor
		image_path: Path to the image file
		prompt: Text prompt to use for inference
		max_new_tokens: Maximum number of tokens to generate
	
	Returns:
		Decoded text response from the model
	"""
	# Load and resize image to standard size
	image = Image.open(image_path).resize((384, 384))
	
	# Prepare inputs (handles both text and image encoding)
	inputs = prepare_inputs(prompt, image, mt.processor, mt.device)
	
	# Generate response without computing gradients (inference only)
	with torch.no_grad():
		output_ids = mt.model.generate(**inputs, max_new_tokens=max_new_tokens)
	
	# Decode only the newly generated tokens (exclude input tokens)
	decoded = mt.processor.batch_decode(
		[out_ids[len(in_ids):] for in_ids, out_ids in zip(inputs["input_ids"], output_ids)],
		skip_special_tokens=True,
		clean_up_tokenization_spaces=False,
	)[0]
	
	return decoded


def ensure_dir(path: str) -> None:
	"""
	Create directory if it doesn't exist.
	
	Args:
		path: Path to directory to create
	"""
	os.makedirs(path, exist_ok=True)


def load_processed_set(results_jsonl_path: str) -> set:
	"""
	Load the set of already processed filenames from a JSONL file.
	This enables resuming interrupted runs without reprocessing images.
	
	Args:
		results_jsonl_path: Path to JSONL file containing previous results
	
	Returns:
		Set of filenames that have already been processed
	"""
	if not os.path.exists(results_jsonl_path):
		return set()
	
	seen = set()
	with open(results_jsonl_path, "r") as f:
		for line in f:
			try:
				record = json.loads(line)
				fname = record.get("filename")
				if fname:
					seen.add(fname)
			except Exception:
				# Skip malformed lines
				continue
	return seen


def append_jsonl(path: str, obj: dict) -> None:
	"""
	Append a JSON object as a new line to a JSONL file.
	
	JSONL format: each line is a separate JSON object, making it easy to
	stream large datasets and resume processing.
	
	Args:
		path: Path to JSONL file
		obj: Dictionary to write as JSON
	"""
	with open(path, "a") as f:
		f.write(json.dumps(obj) + "\n")


def compute_metrics(results: List[dict]) -> Dict[str, float]:
	"""
	Compute classification metrics from the results.
	
	Metrics computed:
	- Accuracy: (TP + TN) / Total
	- Precision: TP / (TP + FP) - how many predicted positives were correct
	- Recall: TP / (TP + FN) - how many actual positives were found
	- F1 Score: harmonic mean of precision and recall
	- True Positives, True Negatives, False Positives, False Negatives
	
	Args:
		results: List of result dictionaries containing 'prediction' and 'expected_label'
	
	Returns:
		Dictionary containing all computed metrics
	"""
	if not results:
		return {
			"accuracy": 0.0,
			"precision": 0.0,
			"recall": 0.0,
			"f1_score": 0.0,
			"true_positives": 0,
			"true_negatives": 0,
			"false_positives": 0,
			"false_negatives": 0,
			"total": 0,
		}
	
	# Initialize counters for confusion matrix
	tp = 0  # True Positives: predicted 1, actual 1
	tn = 0  # True Negatives: predicted 0, actual 0
	fp = 0  # False Positives: predicted 1, actual 0
	fn = 0  # False Negatives: predicted 0, actual 1
	
	# Count each case
	for r in results:
		pred = r["prediction"]
		actual = r["expected_label"]
		
		if pred == 1 and actual == 1:
			tp += 1
		elif pred == 0 and actual == 0:
			tn += 1
		elif pred == 1 and actual == 0:
			fp += 1
		elif pred == 0 and actual == 1:
			fn += 1
	
	total = len(results)
	
	# Calculate metrics with division-by-zero protection
	accuracy = (tp + tn) / total if total > 0 else 0.0
	precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
	recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
	f1_score = (
		2 * precision * recall / (precision + recall)
		if (precision + recall) > 0
		else 0.0
	)
	
	return {
		"accuracy": accuracy,
		"precision": precision,
		"recall": recall,
		"f1_score": f1_score,
		"true_positives": tp,
		"true_negatives": tn,
		"false_positives": fp,
		"false_negatives": fn,
		"total": total,
	}


def print_metrics(metrics: Dict[str, float]) -> None:
	"""
	Pretty-print the evaluation metrics.
	
	Args:
		metrics: Dictionary of metrics from compute_metrics()
	"""
	print("\n" + "="*60)
	print("EVALUATION RESULTS")
	print("="*60)
	print(f"Total Samples:       {metrics['total']}")
	print(f"\nAccuracy:            {metrics['accuracy']:.4f} ({metrics['accuracy']*100:.2f}%)")
	print(f"Precision:           {metrics['precision']:.4f}")
	print(f"Recall:              {metrics['recall']:.4f}")
	print(f"F1 Score:            {metrics['f1_score']:.4f}")
	print(f"\nConfusion Matrix:")
	print(f"  True Positives:    {metrics['true_positives']}")
	print(f"  True Negatives:    {metrics['true_negatives']}")
	print(f"  False Positives:   {metrics['false_positives']}")
	print(f"  False Negatives:   {metrics['false_negatives']}")
	print("="*60 + "\n")


def main(
	model_path: str,
	device: str,
	dataset_dir: str,
	category_mapping_path: str,
	results_jsonl_path: str,
	metrics_json_path: str,
	max_new_tokens: int,
	expected_label: int,
) -> None:
	"""
	Main function to run baseline inference and evaluation.
	
	Workflow:
	1. Load category mapping
	2. Collect all image files
	3. Load the VLM model
	4. Process each image (with resume support)
	5. Compute and save metrics
	6. Print summary
	
	Args:
		model_path: Path or HF ID of the model to evaluate
		device: Device to run on
		dataset_dir: Directory containing test images
		category_mapping_path: Path to category mapping JSON
		results_jsonl_path: Where to save per-sample results
		metrics_json_path: Where to save overall metrics
		max_new_tokens: Max tokens to generate per sample
		expected_label: Ground truth label (0 or 1) for the dataset
	"""
	print(f"Starting baseline inference and evaluation...")
	print(f"Model: {model_path}")
	print(f"Device: {device}")
	print(f"Dataset: {dataset_dir}")
	
	# Load category mapping
	category_mapping = load_category_mapping(category_mapping_path)
	print(f"Loaded {len(category_mapping)} categories")

	# Collect all image files from dataset directory
	image_files = []
	if os.path.exists(dataset_dir):
		image_files = glob.glob(os.path.join(dataset_dir, "*.jpg")) + \
		              glob.glob(os.path.join(dataset_dir, "*.png"))

	if not image_files:
		print(f"ERROR: No images found in {dataset_dir}")
		print("Available directories with images:")
		for root, dirs, files in os.walk("."):
			if any(f.endswith((".jpg", ".png")) for f in files):
				count = len([f for f in files if f.endswith((".jpg", ".png"))])
				print(f"  {root}: {count} images")
		return

	print(f"Found {len(image_files)} images to process")

	# Load the VLM model
	print(f"Loading model from {model_path}...")
	mt = load_vlm(model_path, device)
	print("Model loaded successfully")

	# Resume support: check which images have already been processed
	processed = load_processed_set(results_jsonl_path)
	print(f"Already processed {len(processed)} images")
	to_process = [p for p in image_files if os.path.basename(p) not in processed]
	print(f"Remaining images to process: {len(to_process)}")

	# Ensure output directories exist
	ensure_dir(os.path.dirname(results_jsonl_path))
	ensure_dir(os.path.dirname(metrics_json_path))

	# Initialize sample counter (continues from where we left off)
	sample_id = len(processed)

	# Process each image
	print("\nRunning inference...")
	for idx, img_path in enumerate(tqdm(to_process, desc="Processing images")):
		fname = os.path.basename(img_path)
		try:
			# Extract category from filename and look up category name
			category_id = get_category_from_filename(fname)
			category = category_mapping.get(category_id, "unknown")
			
			# Build the prompt for this image
			prompt = f"Is there a {category} in the image? Answer yes or no."

			# Run model inference
			response_text = run_single_inference(mt, img_path, prompt, max_new_tokens=max_new_tokens)
			
			# Convert response to binary prediction
			prediction = evaluate_response(response_text, category)

			# Save detailed result for this sample
			result = {
				"sample_id": sample_id,
				"filename": fname,
				"image_path": img_path,
				"category_id": category_id,
				"category": category,
				"prompt": prompt,
				"response": response_text,
				"prediction": prediction,
				"expected_label": expected_label,
				"correct": int(prediction == expected_label),
			}
			append_jsonl(results_jsonl_path, result)

			sample_id += 1

		except Exception as e:
			print(f"\nError processing {fname}: {e}")
			continue

	# Load all results and compute metrics
	print("\nComputing metrics...")
	all_results = []
	with open(results_jsonl_path, "r") as f:
		for line in f:
			try:
				all_results.append(json.loads(line))
			except Exception:
				continue

	metrics = compute_metrics(all_results)
	
	# Save metrics to JSON file
	with open(metrics_json_path, "w") as f:
		json.dump(metrics, f, indent=2)
	
	print(f"Metrics saved to: {metrics_json_path}")
	print(f"Detailed results saved to: {results_jsonl_path}")
	
	# Print metrics to console
	print_metrics(metrics)


